#include "type.h"

namespace android{

	void android_printf(int level, char *format, ...)
	{
		if (level >= MSG_DUMP) {
			va_list ap;
			if (level == MSG_ERROR) {
				level = ANDROID_LOG_WARN;
			} else if (level == MSG_WARNING) {
				level = ANDROID_LOG_WARN;
			} else if (level == MSG_INFO) {
				level = ANDROID_LOG_INFO;
			} else {
				level = ANDROID_LOG_DEBUG;
			}
			va_start(ap, format);
			__android_log_vprint(level, "wifi_em", format, ap);
			va_end(ap);
		}
	}
	
	void em_error(char *format, ...)
	{
		va_list ap;
	
		va_start(ap, format);
		__android_log_vprint(MSG_WARNING, "wifi_em", format, ap);
		va_end(ap);
	}


}
