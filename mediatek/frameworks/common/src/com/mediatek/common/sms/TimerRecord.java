package com.mediatek.common.sms;

/*
* This class is used to record concatenated sms
*/
public class TimerRecord {
    public String address;
    public int refNumber;
    public int msgCount;
    
    public TimerRecord(String address, int refNumber, int msgCount) {
        this.address = address;
        this.refNumber = refNumber;
        this.msgCount = msgCount;
    }
    
    public boolean equals(TimerRecord other) {
        if(other == null) {
            return false;
        }
        
        return (this.address.equals(other.address))
                && (this.refNumber == other.refNumber);
    }
    
    public String toString() {
        StringBuilder sb = new StringBuilder(100);
        sb.append("TimerRecord: ");
        sb.append("address = ");
        sb.append(address);
        sb.append(", refNumber = ");
        sb.append(refNumber);
        sb.append(", msgCount = ");
        sb.append(msgCount);
        return sb.toString();
    }
}