package com.mediatek.ngin3d.presentation;

import android.graphics.Canvas;

/**
 * Provides 2D drawing.
 */
public interface Graphics2d extends ImageDisplay {

    /**
     * Begin drawing on the canvas with specified width and height.
     *
     * @param width in pixels
     * @param height in pixels
     * @param backgroundColor background color
     * @return the canvas to draw
     */
    Canvas beginDraw(int width, int height, int backgroundColor);

    /**
     * Finish the drawing.
     */
    void endDraw();

    /**
     * The fade out effect Uses linear interpolation,
     * start from x1 with alpha 1 and end at x2 point with alpha 0.
     * The range of x1 and x2 are 0~1 and x2 must be bigger than x1.
     *
     * @param x1 the fade out start point
     * @param x2 the fade out end point
     */
    void setFadeOutCoord(float x1, float x2);

}
