package com.mediatek.ngin3d.j3m;

import com.mediatek.ngin3d.presentation.Graphics3d;

/**
 * The presentation that provides 3D graphics drawing.
 * @hide
 */
// \todo implement
public class Canvas3dPresentation extends ActorPresentation
    implements Graphics3d {

    private Renderer mRenderer;

    public Canvas3dPresentation(J3mPresentationEngine engine) {
        super(engine);
    }

    /**
     * Un-initialize this object
     */
    @Override
    public void onUninitialize() {
        mRenderer = null;
        super.onUninitialize();
    }

    public void setRenderer(Renderer renderer) {
        mRenderer = renderer;
    }

    public void render(int frame) {
        // do nothing by default
    }

    public void onDrawFrame() {
        if (mRenderer != null) {
            mRenderer.onDrawFrame(this);
        }
    }
}
